@dataclass(frozen=True)
class InstanceMetrics:
    """Decision-level metrics for one randomized toy instance."""

    objective_sense: str
    oracle_solution: str
    selected_solution: str
    oracle_objective: float
    selected_true_objective: float
    normalized_regret: float
    true_second_best_normalized_gap: float
    identity_mismatch: bool
    changed_component_count: int


@dataclass(frozen=True)
class SweepResult:
    """All rows generated by one parameter sweep."""

    packing_summary_rows: list[dict]
    stable_set_summary_rows: list[dict]
    shortest_path_summary_rows: list[dict]
    summary_rows: list[dict]
    instance_rows: list[dict]


def stable_seed(*parts: object) -> int:
    """Return a deterministic seed independent of Python's hash randomization."""
    text = "|".join(str(part) for part in parts)
    digest = hashlib.sha256(text.encode("utf-8")).hexdigest()
    return int(digest[:16], 16)


def argmax_index(values: Sequence[float]) -> int:
    if not values:
        raise ValueError("argmax_index requires at least one value")
    return max(range(len(values)), key=lambda idx: (values[idx], -idx))


def argmin_index(values: Sequence[float]) -> int:
    if not values:
        raise ValueError("argmin_index requires at least one value")
    return min(range(len(values)), key=lambda idx: (values[idx], idx))


def normalized_gap_maximization(oracle_value: float, selected_value: float) -> float:
    if abs(oracle_value) < 1e-12:
        return 0.0
    return max(0.0, float((oracle_value - selected_value) / abs(oracle_value)))


def normalized_gap_minimization(oracle_cost: float, selected_cost: float) -> float:
    if abs(oracle_cost) < 1e-12:
        return 0.0
    return max(0.0, float((selected_cost - oracle_cost) / abs(oracle_cost)))


def validate_rectangular_matrix(values: Sequence[Sequence[float]], name: str) -> None:
    if not values:
        raise ValueError(f"{name} must contain at least one row")
    row_length = len(values[0])
    if row_length == 0:
        raise ValueError(f"{name} rows must contain at least one value")
    for row in values:
        if len(row) != row_length:
            raise ValueError(f"{name} must be rectangular")


def evaluate_partition_instance(
    true_values: Sequence[Sequence[float]],
    predicted_values: Sequence[Sequence[float]],
) -> InstanceMetrics:
    """Evaluate one decomposable packing / partition-matroid instance.

    A feasible solution selects one component from each independent block.
    The oracle and 2stage decisions are therefore block-wise argmax decisions.
    The true second-best solution differs from the oracle in the block with the
    smallest local best-vs-second-best true-value gap.
    """
    validate_rectangular_matrix(true_values, "true_values")
    validate_rectangular_matrix(predicted_values, "predicted_values")
    if len(true_values) != len(predicted_values) or len(true_values[0]) != len(
        predicted_values[0]
    ):
        raise ValueError("true_values and predicted_values must have the same shape")

    oracle_choices: list[int] = []
    selected_choices: list[int] = []
    oracle_value = 0.0
    selected_value = 0.0
    local_second_best_gaps: list[float] = []

    for true_row, predicted_row in zip(true_values, predicted_values):
        true_choice = argmax_index(true_row)
        predicted_choice = argmax_index(predicted_row)
        oracle_choices.append(true_choice)
        selected_choices.append(predicted_choice)
        oracle_value += float(true_row[true_choice])
        selected_value += float(true_row[predicted_choice])

        sorted_true = sorted((float(value) for value in true_row), reverse=True)
        if len(sorted_true) >= 2:
            local_second_best_gaps.append(sorted_true[0] - sorted_true[1])

    changed_count = sum(
        1
        for oracle_choice, selected_choice in zip(oracle_choices, selected_choices)
        if oracle_choice != selected_choice
    )
    second_gap = min(local_second_best_gaps) if local_second_best_gaps else 0.0
    normalized_second_gap = second_gap / abs(oracle_value) if abs(oracle_value) >= 1e-12 else 0.0

    return InstanceMetrics(
        objective_sense="maximize",
        oracle_solution=",".join(str(choice) for choice in oracle_choices),
        selected_solution=",".join(str(choice) for choice in selected_choices),
        oracle_objective=oracle_value,
        selected_true_objective=selected_value,
        normalized_regret=normalized_gap_maximization(oracle_value, selected_value),
        true_second_best_normalized_gap=float(normalized_second_gap),
        identity_mismatch=changed_count > 0,
        changed_component_count=changed_count,
    )


def evaluate_clustered_stable_set_instance(
    true_values: Sequence[Sequence[float]],
    predicted_values: Sequence[Sequence[float]],
) -> InstanceMetrics:
    """Evaluate a stable-set realization of the close-substitute mechanism.

    The conflict graph is a disjoint union of cliques. Each row is one clique
    (one substitution block), so an independent set can choose at most one vertex
    from each clique. Since generated weights are positive, the oracle and
    predicted independent sets choose exactly one vertex per clique. This is a
    stable-set / independent-set encoding of a decomposable packing problem.
    """
    return evaluate_partition_instance(true_values, predicted_values)


def evaluate_parallel_path_instance(
    true_path_costs: Sequence[float],
    predicted_path_costs: Sequence[float],
    path_length: int,
) -> InstanceMetrics:
    """Evaluate one parallel shortest-path instance.

    Each path is a coupled s-t route. If 2stage picks a different path from the
    oracle, all path_length edge components change together.
    """
    if len(true_path_costs) < 2:
        raise ValueError("true_path_costs must contain at least two paths")
    if len(true_path_costs) != len(predicted_path_costs):
        raise ValueError("true_path_costs and predicted_path_costs must have equal length")
    if path_length <= 0:
        raise ValueError("path_length must be positive")

    oracle_idx = argmin_index(true_path_costs)
    selected_idx = argmin_index(predicted_path_costs)
    oracle_cost = float(true_path_costs[oracle_idx])
    selected_cost = float(true_path_costs[selected_idx])
    sorted_true = sorted(float(cost) for cost in true_path_costs)
    second_gap = sorted_true[1] - sorted_true[0]
    normalized_second_gap = second_gap / abs(oracle_cost) if abs(oracle_cost) >= 1e-12 else 0.0

    mismatch = oracle_idx != selected_idx
    return InstanceMetrics(
        objective_sense="minimize",
        oracle_solution=f"path_{oracle_idx}",
        selected_solution=f"path_{selected_idx}",
        oracle_objective=oracle_cost,
        selected_true_objective=selected_cost,
        normalized_regret=normalized_gap_minimization(oracle_cost, selected_cost),
        true_second_best_normalized_gap=float(normalized_second_gap),
        identity_mismatch=mismatch,
        changed_component_count=path_length if mismatch else 0,
    )


def draw_positive_value_matrix(
    rng: random.Random,
    *,
    rows: int,
    cols: int,
    tau: float,
    sigma: float,
) -> tuple[list[tuple[float, ...]], list[tuple[float, ...]]]:
    if rows <= 0:
        raise ValueError("rows must be positive")
    if cols < 2:
        raise ValueError("cols must be at least two")
    if tau < 0.0 or sigma < 0.0:
        raise ValueError("tau and sigma must be non-negative")

    true_values: list[tuple[float, ...]] = []
    predicted_values: list[tuple[float, ...]] = []
    for _ in range(rows):
        true_row = tuple(max(1e-6, rng.uniform(1.0 - tau, 1.0 + tau)) for _ in range(cols))
        predicted_row = tuple(value + rng.gauss(0.0, sigma) for value in true_row)
        true_values.append(true_row)
        predicted_values.append(predicted_row)
    return true_values, predicted_values


def draw_partition_instance(
    rng: random.Random,
    *,
    blocks: int,
    choices: int,
    tau: float,
    sigma: float,
) -> InstanceMetrics:
    """Draw and evaluate a random decomposable packing instance."""
    true_values, predicted_values = draw_positive_value_matrix(
        rng, rows=blocks, cols=choices, tau=tau, sigma=sigma
    )
    return evaluate_partition_instance(true_values, predicted_values)


def draw_clustered_stable_set_instance(
    rng: random.Random,
    *,
    cliques: int,
    vertices_per_clique: int,
    tau: float,
    sigma: float,
) -> InstanceMetrics:
    """Draw and evaluate a random clustered stable-set instance."""
    true_values, predicted_values = draw_positive_value_matrix(
        rng, rows=cliques, cols=vertices_per_clique, tau=tau, sigma=sigma
    )
    return evaluate_clustered_stable_set_instance(true_values, predicted_values)


def draw_parallel_path_instance(
    rng: random.Random,
    *,
    path_count: int,
    path_length: int,
    tau: float,
    sigma: float,
) -> InstanceMetrics:
    """Draw and evaluate a random coupled parallel shortest-path instance."""
    if path_count < 2:
        raise ValueError("path_count must be at least two")
    if path_length <= 0:
        raise ValueError("path_length must be positive")
    if tau < 0.0 or sigma < 0.0:
        raise ValueError("tau and sigma must be non-negative")

    true_path_costs: list[float] = []
    predicted_path_costs: list[float] = []
    for _ in range(path_count):
        path_bias = rng.uniform(0.0, tau)
        true_cost = 0.0
        predicted_cost = 0.0
        for _ in range(path_length):
            local_deviation = rng.uniform(-0.25 * tau, 0.25 * tau)
            edge_cost = max(1e-6, 1.0 + path_bias + local_deviation)
            true_cost += edge_cost
            predicted_cost += edge_cost + rng.gauss(0.0, sigma)
        true_path_costs.append(true_cost)
        predicted_path_costs.append(predicted_cost)
    return evaluate_parallel_path_instance(true_path_costs, predicted_path_costs, path_length)


def finite_mean(values: Iterable[float]) -> float:
    values = list(values)
    return float(sum(values) / len(values)) if values else 0.0


def finite_median(values: Iterable[float]) -> float:
    values = sorted(values)
    if not values:
        return 0.0
    return float(statistics.median(values))


def finite_quantile(values: Iterable[float], quantile: float) -> float:
    values = sorted(values)
    if not values:
        return 0.0
    if len(values) == 1:
        return float(values[0])
    position = (len(values) - 1) * quantile
    lower = int(position)
    upper = min(lower + 1, len(values) - 1)
    weight = position - lower
    return float(values[lower] * (1.0 - weight) + values[upper] * weight)


def substitute_regime(tau: float) -> str:
    if tau <= 0.05 + 1e-12:
        return "close_substitutes"
    if tau >= 0.20 - 1e-12:
        return "separated_alternatives"
    return "moderate_substitutes"


def problem_label(problem_family: str) -> str:
    labels = {
        PACKING_FAMILY: "Random decomposable packing",
        STABLE_SET_FAMILY: "Random stable/independent set",
        PATH_FAMILY: "Random parallel shortest path",
    }
    try:
        return labels[problem_family]
    except KeyError as exc:
        raise ValueError(f"Unknown problem family: {problem_family}") from exc


def summarize_metrics(
    metrics: list[InstanceMetrics],
    *,
    problem_family: str,
    tau: float,
    sigma: float,
) -> dict:
    if not metrics:
        raise ValueError("Cannot summarize an empty metrics list")

    regrets = [metric.normalized_regret for metric in metrics]
    mismatch_regrets = [
        metric.normalized_regret for metric in metrics if metric.identity_mismatch
    ]
    rank2_gaps = [metric.true_second_best_normalized_gap for metric in metrics]
    mismatch_values = [1.0 if metric.identity_mismatch else 0.0 for metric in metrics]
    changed_counts = [float(metric.changed_component_count) for metric in metrics]
    oracle_objectives = [metric.oracle_objective for metric in metrics]

    return {
        "problem_family": problem_family,
        "problem_label": problem_label(problem_family),
        "objective_sense": metrics[0].objective_sense,
        "tau": float(tau),
        "sigma": float(sigma),
        "substitute_regime": substitute_regime(tau),
        "num_instances": len(metrics),
        "identity_mismatch_rate": finite_mean(mismatch_values),
        "mean_normalized_regret": finite_mean(regrets),
        "q25_normalized_regret": finite_quantile(regrets, 0.25),
        "median_normalized_regret": finite_median(regrets),
        "q75_normalized_regret": finite_quantile(regrets, 0.75),
        "p90_normalized_regret": finite_quantile(regrets, 0.90),
        "max_normalized_regret": max(regrets),
        "mean_normalized_regret_when_mismatch": finite_mean(mismatch_regrets),
        "median_normalized_regret_when_mismatch": finite_median(mismatch_regrets),
        "mean_true_second_best_normalized_gap": finite_mean(rank2_gaps),
        "q25_true_second_best_normalized_gap": finite_quantile(rank2_gaps, 0.25),
        "median_true_second_best_normalized_gap": finite_median(rank2_gaps),
        "q75_true_second_best_normalized_gap": finite_quantile(rank2_gaps, 0.75),
        "p90_true_second_best_normalized_gap": finite_quantile(rank2_gaps, 0.90),
        "within_1pct_rate": finite_mean(
            1.0 if regret <= 0.01 + 1e-12 else 0.0 for regret in regrets
        ),
        "within_5pct_rate": finite_mean(
            1.0 if regret <= 0.05 + 1e-12 else 0.0 for regret in regrets
        ),
        "mean_dfl_improvement_ceiling": finite_mean(regrets),
        "median_dfl_improvement_ceiling": finite_median(regrets),
        "mean_changed_component_count": finite_mean(changed_counts),
        "mean_oracle_objective": finite_mean(oracle_objectives),
    }


def metrics_to_instance_row(
    metric: InstanceMetrics,
    *,
    problem_family: str,
    tau: float,
    sigma: float,
    instance_id: int,
) -> dict:
    return {
        "problem_family": problem_family,
        "tau": tau,
        "sigma": sigma,
        "instance_id": instance_id,
        "objective_sense": metric.objective_sense,
        "oracle_solution": metric.oracle_solution,
        "selected_solution": metric.selected_solution,
        "oracle_objective": metric.oracle_objective,
        "selected_true_objective": metric.selected_true_objective,
        "normalized_regret": metric.normalized_regret,
        "true_second_best_normalized_gap": metric.true_second_best_normalized_gap,
        "identity_mismatch": metric.identity_mismatch,
        "changed_component_count": metric.changed_component_count,
    }


def run_sweep(
    *,
    tau_values: Sequence[float] = DEFAULT_TAU_VALUES,
    sigma_values: Sequence[float] = DEFAULT_SIGMA_VALUES,
    num_instances: int = 500,
    base_seed: int = 1729,
    packing_blocks: int = 12,
    packing_choices: int = 4,
    stable_cliques: int = 12,
    stable_vertices_per_clique: int = 4,
    path_count: int = 4,
    path_length: int = 12,
) -> SweepResult:
    if num_instances <= 0:
        raise ValueError("num_instances must be positive")

    packing_summary_rows: list[dict] = []
    stable_set_summary_rows: list[dict] = []
    shortest_path_summary_rows: list[dict] = []
    instance_rows: list[dict] = []

    for tau in tau_values:
        for sigma in sigma_values:
            packing_rng = random.Random(stable_seed(base_seed, PACKING_FAMILY, tau, sigma))
            packing_metrics = [
                draw_partition_instance(
                    packing_rng,
                    blocks=packing_blocks,
                    choices=packing_choices,
                    tau=float(tau),
                    sigma=float(sigma),
                )
                for _ in range(num_instances)
            ]
            packing_summary_rows.append(
                summarize_metrics(
                    packing_metrics,
                    problem_family=PACKING_FAMILY,
                    tau=float(tau),
                    sigma=float(sigma),
                )
            )
            for instance_id, metric in enumerate(packing_metrics):
                instance_rows.append(
                    metrics_to_instance_row(
                        metric,
                        problem_family=PACKING_FAMILY,
                        tau=float(tau),
                        sigma=float(sigma),
                        instance_id=instance_id,
                    )
                )

            stable_rng = random.Random(stable_seed(base_seed, STABLE_SET_FAMILY, tau, sigma))
            stable_metrics = [
                draw_clustered_stable_set_instance(
                    stable_rng,
                    cliques=stable_cliques,
                    vertices_per_clique=stable_vertices_per_clique,
                    tau=float(tau),
                    sigma=float(sigma),
                )
                for _ in range(num_instances)
            ]
            stable_set_summary_rows.append(
                summarize_metrics(
                    stable_metrics,
                    problem_family=STABLE_SET_FAMILY,
                    tau=float(tau),
                    sigma=float(sigma),
                )
            )
            for instance_id, metric in enumerate(stable_metrics):
                instance_rows.append(
                    metrics_to_instance_row(
                        metric,
                        problem_family=STABLE_SET_FAMILY,
                        tau=float(tau),
                        sigma=float(sigma),
                        instance_id=instance_id,
                    )
                )

            path_rng = random.Random(stable_seed(base_seed, PATH_FAMILY, tau, sigma))
            path_metrics = [
                draw_parallel_path_instance(
                    path_rng,
                    path_count=path_count,
                    path_length=path_length,
                    tau=float(tau),
                    sigma=float(sigma),
                )
                for _ in range(num_instances)
            ]
            shortest_path_summary_rows.append(
                summarize_metrics(
                    path_metrics,
                    problem_family=PATH_FAMILY,
                    tau=float(tau),
                    sigma=float(sigma),
                )
            )
            for instance_id, metric in enumerate(path_metrics):
                instance_rows.append(
                    metrics_to_instance_row(
                        metric,
                        problem_family=PATH_FAMILY,
                        tau=float(tau),
                        sigma=float(sigma),
                        instance_id=instance_id,
                    )
                )

    summary_rows = packing_summary_rows + stable_set_summary_rows + shortest_path_summary_rows
    return SweepResult(
        packing_summary_rows=packing_summary_rows,
        stable_set_summary_rows=stable_set_summary_rows,
        shortest_path_summary_rows=shortest_path_summary_rows,
        summary_rows=summary_rows,
        instance_rows=instance_rows,
    )


def ensure_dirs(result_dir: Path, plot_dir: Path) -> None:
    result_dir.mkdir(parents=True, exist_ok=True)
    plot_dir.mkdir(parents=True, exist_ok=True)


def write_csv(path: Path, rows: list[dict], fieldnames: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def format_float_label(value: float) -> str:
    return f"{value:g}"


def selected_focus_taus(summary_rows: list[dict]) -> list[float]:
    taus = sorted({float(row["tau"]) for row in summary_rows})
    if len(taus) <= 2:
        return taus
    return [taus[0], taus[-1]]


def selected_focus_sigma(summary_rows: list[dict]) -> float:
    sigmas = sorted({float(row["sigma"]) for row in summary_rows})
    positive = [sigma for sigma in sigmas if sigma > 0.0]
    return positive[len(positive) // 2] if positive else sigmas[0]


def ordered_families(summary_rows: list[dict]) -> list[str]:
    present = {row["problem_family"] for row in summary_rows}
    return [family for family in FAMILY_ORDER if family in present]


def family_short_label(problem_family: str) -> str:
    return {
        PACKING_FAMILY: "packing",
        STABLE_SET_FAMILY: "stable set",
        PATH_FAMILY: "path",
    }[problem_family]


def family_plot_title(problem_family: str) -> str:
    return {
        PACKING_FAMILY: "Decomposable packing",
        STABLE_SET_FAMILY: "Stable / independent set",
        PATH_FAMILY: "Parallel shortest path",
    }[problem_family]


def make_identity_mismatch_vs_regret_plot(summary_rows: list[dict], plot_dir: Path) -> None:
    try:
        import matplotlib.pyplot as plt
    except ImportError as exc:
        raise RuntimeError(
            "matplotlib is required to generate plots. Install it or rerun with --skip-plots."
        ) from exc

    focus_taus = selected_focus_taus(summary_rows)
    families = ordered_families(summary_rows)
    metrics = [
        ("identity_mismatch_rate", "Identity mismatch rate (%)"),
        ("mean_normalized_regret", "Mean normalized regret (%)"),
    ]

    fig, axes = plt.subplots(1, 2, figsize=(11.5, 4.4), sharex=True)
    for ax, (metric_key, ylabel) in zip(axes, metrics):
        for family in families:
            for tau in focus_taus:
                rows = sorted(
                    (
                        row
                        for row in summary_rows
                        if row["problem_family"] == family and abs(float(row["tau"]) - tau) < 1e-12
                    ),
                    key=lambda row: float(row["sigma"]),
                )
                if not rows:
                    continue
                sigmas = [float(row["sigma"]) for row in rows]
                values = [100.0 * float(row[metric_key]) for row in rows]
                ax.plot(
                    sigmas,
                    values,
                    marker="o",
                    label=f"{family_short_label(family)}, tau={format_float_label(tau)}",
                )
        ax.set_xlabel("Prediction noise sigma")
        ax.set_ylabel(ylabel)
        ax.grid(True, linestyle="--", linewidth=0.5, alpha=0.6)
    axes[0].set_title("Ranking flips")
    axes[1].set_title("Regret / DFL improvement ceiling")
    axes[1].legend(fontsize=8)
    fig.suptitle("Randomized Property X toy families")
    fig.tight_layout()
    fig.savefig(plot_dir / "identity_mismatch_vs_regret.png", dpi=200)
    plt.close(fig)


def make_rank2_gap_distribution_plot(
    instance_rows: list[dict],
    summary_rows: list[dict],
    plot_dir: Path,
) -> None:
    try:
        import matplotlib.pyplot as plt
    except ImportError as exc:
        raise RuntimeError(
            "matplotlib is required to generate plots. Install it or rerun with --skip-plots."
        ) from exc

    focus_taus = selected_focus_taus(summary_rows)
    focus_sigma = selected_focus_sigma(summary_rows)
    families = ordered_families(summary_rows)

    labels: list[str] = []
    values_by_group: list[list[float]] = []
    for tau in focus_taus:
        for family in families:
            values = [
                100.0 * float(row["true_second_best_normalized_gap"])
                for row in instance_rows
                if row["problem_family"] == family
                and abs(float(row["tau"]) - tau) < 1e-12
                and abs(float(row["sigma"]) - focus_sigma) < 1e-12
            ]
            if values:
                labels.append(f"{family_short_label(family)}\ntau={format_float_label(tau)}")
                values_by_group.append(values)

    fig, ax = plt.subplots(figsize=(9.5, 4.8))
    ax.boxplot(values_by_group, showfliers=False)
    ax.set_xticks(range(1, len(labels) + 1))
    ax.set_xticklabels(labels)
    ax.set_ylabel("Oracle-second-best true gap (%)")
    ax.set_title(
        "Rank-2 gap distribution at sigma="
        f"{format_float_label(focus_sigma)}"
    )
    ax.grid(True, axis="y", linestyle="--", linewidth=0.5, alpha=0.6)
    fig.tight_layout()
    fig.savefig(plot_dir / "rank2_gap_distribution.png", dpi=200)
    plt.close(fig)


def make_property_x_phase_diagram(summary_rows: list[dict], plot_dir: Path) -> None:
    try:
        import matplotlib.pyplot as plt
    except ImportError as exc:
        raise RuntimeError(
            "matplotlib is required to generate plots. Install it or rerun with --skip-plots."
        ) from exc

    taus = sorted({float(row["tau"]) for row in summary_rows})
    sigmas = sorted({float(row["sigma"]) for row in summary_rows})
    families = ordered_families(summary_rows)

    max_value = max(100.0 * float(row["mean_dfl_improvement_ceiling"]) for row in summary_rows)
    fig_width = max(11.0, 4.0 * len(families))
    fig, axes = plt.subplots(1, len(families), figsize=(fig_width, 4.8), sharey=True)
    if len(families) == 1:
        axes = [axes]

    image = None
    for ax, family in zip(axes, families):
        by_key = {
            (float(row["tau"]), float(row["sigma"])): 100.0
            * float(row["mean_dfl_improvement_ceiling"])
            for row in summary_rows
            if row["problem_family"] == family
        }
        matrix = [
            [by_key.get((tau, sigma), 0.0) for tau in taus]
            for sigma in sigmas
        ]
        image = ax.imshow(matrix, origin="lower", aspect="auto", vmin=0.0, vmax=max_value)
        ax.set_title(family_plot_title(family))
        ax.set_xlabel("Alternative separation tau")
        ax.set_xticks(range(len(taus)))
        ax.set_xticklabels([format_float_label(tau) for tau in taus])
        ax.set_yticks(range(len(sigmas)))
        ax.set_yticklabels([format_float_label(sigma) for sigma in sigmas])
        for y_index, sigma in enumerate(sigmas):
            for x_index, tau in enumerate(taus):
                value = by_key.get((tau, sigma), 0.0)
                ax.text(x_index, y_index, f"{value:.1f}", ha="center", va="center", fontsize=8)
    axes[0].set_ylabel("Prediction noise sigma")
    if image is not None:
        cbar = fig.colorbar(image, ax=axes, shrink=0.85)
        cbar.set_label("Mean DFL improvement ceiling (%)")
    fig.suptitle("Regret room depends on substitute gaps and prediction noise")
    fig.savefig(plot_dir / "property_x_phase_diagram.png", dpi=200, bbox_inches="tight")
    plt.close(fig)


def make_plots(result: SweepResult, plot_dir: Path) -> None:
    make_identity_mismatch_vs_regret_plot(result.summary_rows, plot_dir)
    make_rank2_gap_distribution_plot(result.instance_rows, result.summary_rows, plot_dir)
    make_property_x_phase_diagram(result.summary_rows, plot_dir)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Run randomized Property X toy-family experiments."
    )
    parser.add_argument("--result-dir", type=Path, default=DEFAULT_RESULT_DIR)
    parser.add_argument("--plot-dir", type=Path, default=DEFAULT_PLOT_DIR)
    parser.add_argument("--num-instances", type=int, default=500)
    parser.add_argument("--base-seed", type=int, default=1729)
    parser.add_argument("--tau-values", nargs="+", type=float, default=DEFAULT_TAU_VALUES)
    parser.add_argument("--sigma-values", nargs="+", type=float, default=DEFAULT_SIGMA_VALUES)
    parser.add_argument("--packing-blocks", type=int, default=12)
    parser.add_argument("--packing-choices", type=int, default=4)
    parser.add_argument("--stable-cliques", type=int, default=12)
    parser.add_argument("--stable-vertices-per-clique", type=int, default=4)
    parser.add_argument("--path-count", type=int, default=4)
    parser.add_argument("--path-length", type=int, default=12)
    parser.add_argument("--skip-plots", action="store_true")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    ensure_dirs(args.result_dir, args.plot_dir)

    result = run_sweep(
        tau_values=args.tau_values,
        sigma_values=args.sigma_values,
        num_instances=args.num_instances,
        base_seed=args.base_seed,
        packing_blocks=args.packing_blocks,
        packing_choices=args.packing_choices,
        stable_cliques=args.stable_cliques,
        stable_vertices_per_clique=args.stable_vertices_per_clique,
        path_count=args.path_count,
        path_length=args.path_length,
    )

    write_csv(
        args.result_dir / "randomized_packing_summary.csv",
        result.packing_summary_rows,
        SUMMARY_FIELDS,
    )
    write_csv(
        args.result_dir / "randomized_stable_set_summary.csv",
        result.stable_set_summary_rows,
        SUMMARY_FIELDS,
    )
    write_csv(
        args.result_dir / "randomized_shortest_path_summary.csv",
        result.shortest_path_summary_rows,
        SUMMARY_FIELDS,
    )
    write_csv(
        args.result_dir / "randomized_property_x_comparison.csv",
        result.summary_rows,
        SUMMARY_FIELDS,
    )

    if not args.skip_plots:
        make_plots(result, args.plot_dir)

    print(f"Saved randomized toy CSVs to: {args.result_dir}")
    if not args.skip_plots:
        print(f"Saved randomized toy plots to: {args.plot_dir}")

    print("\nMean DFL improvement ceiling by family at largest tau/sigma:")
    max_tau = max(float(value) for value in args.tau_values)
    max_sigma = max(float(value) for value in args.sigma_values)
    for row in result.summary_rows:
        if abs(float(row["tau"]) - max_tau) < 1e-12 and abs(float(row["sigma"]) - max_sigma) < 1e-12:
            print(
                f"  {row['problem_family']}: "
                f"{100.0 * float(row['mean_dfl_improvement_ceiling']):.2f}% "
                f"(identity mismatch {100.0 * float(row['identity_mismatch_rate']):.1f}%)"
            )

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
