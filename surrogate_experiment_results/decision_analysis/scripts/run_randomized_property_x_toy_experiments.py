#!/usr/bin/env python3
"""Run randomized toy-family experiments for the Property X mechanism.

The hand-designed toy examples in build_toy_property_x_examples.py are useful
for intuition, but they are not evidence that the mechanism is typical. This
script turns the same idea into stochastic instance families:

  1. A decomposable packing / partition-matroid family.
  2. A clustered stable-set / independent-set family.
  3. A parallel shortest-path family with path-level coupling.

All families are controlled by tau, which controls alternative separation, and
sigma, which controls prediction noise. The key reported quantities are
solution-identity mismatch, normalized regret, the oracle-vs-second-best true
gap, and the maximum possible DFL improvement over 2stage.

Outputs
-------
surrogate_experiment_results/decision_analysis/results/toy_randomized/
  randomized_packing_summary.csv
  randomized_stable_set_summary.csv
  randomized_shortest_path_summary.csv
  randomized_property_x_comparison.csv

surrogate_experiment_results/decision_analysis/plots/toy_randomized/
  identity_mismatch_vs_regret.png
  rank2_gap_distribution.png
  property_x_phase_diagram.png
"""

